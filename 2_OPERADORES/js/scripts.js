// 1 - number
console.log(typeof 2);
console.log(typeof 5.8);
console.log(typeof -127);

// 2 - Ops aritiméticas
console.log(2 + 4);
console.log(10 - 5);
console.log(5 * 4);
console.log(10 / 2);
console.log(8 * 2);

// 3 - Special number
console.log(typeof Infinity);

console.log(typeof - Infinity);

console.log(12 * "ads");

console.log(typeof NaN);

// 4 - Strings
console.log("Um texto");
console.log("outro texto");
console.log(typeof "13");

console.log(typeof "texto")

// 5 - simbolos especiais em string
console.log("quebrando o \n texto");
console.log("texto \t espaçado");

// 6 - Concatenação

console.log("oi," + "tudo" + "bem?");

// 7 - Templates Strings

console.log(`A soma de 2 + 2 é: ${2 + 2}`);
console.log(`Podemos executar qualquer coisa ${console.log("teste")}`);

// 8 - Booleans
console.log(5 > 2);
console.log(30 > 100);
console.log(typeof true);

// 9 - comparações

console.log(5 <= 5);

console.log(5 < 5);

console.log(10 == 10);

console.log(10 == 9);

console.log(10 != 9);

//10 - Idêntico

console.log(10 == "10")

console.log(9 == "9");

console.log(9 != "9")

console.log(9 !== "9")

//11 - op logico na prática

console.log(true && true);

console.log(true && false);

console.log(5 > 2 && 2 < 10);

console.log(5 > 2 && "Patrick" === 1);

console.log(10 > 2 || 5 > 100);

console.log(!50 > 10);

// 12 - empty values
console.log(typeof null, typeof undefined);

console.log(null === undefined);

console.log(null == undefined);

console.log(null == false);

console.log(undefined == false);

// 13 - mudança de tipos
console.log(5 * null);

console.log("teste" * "opa");

console.log("10" + 1);

console.log("10" - 1);


